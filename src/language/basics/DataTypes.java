package language.basics;

public class DataTypes {

	public static void main(String[] args) {
		
		System.out.println("DATA TYPES");
		
		// 1. Temperature of a city in degrees Celsius: 25.5
		float temperature = 25.5f;
		System.out.println("1.  Temperature of the city in degree celcius: "+temperature);
		
		//2. Whether a customer has placed an order: true or false
		boolean orderPlaced = true;
		System.out.println("2.  Customer order status: "+orderPlaced);

		//3. Person's phone number: "123-456-7890"
		long phoneNumber = 9790223430L;
		System.out.println("3.  Person's Phone Number: "+phoneNumber);
		
		//4.  Amount of money in a customer's bank account: 1000.50
		double amount = 1000.50;
		System.out.println("4.  Amount of money in customer's bank account: "+amount);

		//5.  Person's email address: "john.doe@example.com"
		String email = "john.doe@gmail.com";
		System.out.println("5.  Person's email address: "+email);
		
		//6.  Coordinates of a location (latitude, longitude): 37.7749, -122.4194
		float latitude = 37.7749f;
		float longitude = -122.4194f;
		System.out.println("6.  Coordinates of a location: "+latitude+", "+longitude);
		
		//7.  Person's marital status: true or false
		boolean maritalStatus = true;
		System.out.println("7.  Person's marital status: "+maritalStatus);
		
		//8.  Person's occupation: "Software Engineer"
		String occupation = "Software Engineer";
		System.out.println("8.  Person's occupation: "+occupation);
		
		//9.  Person's favourite colour: "Blue"
		String colour = "Blue";
		System.out.println("9.  Person's favourite colour: "+colour);
		
		//10. Current year: 2025
		int currentYear = 2025;
		System.out.println("10. Current Year: "+currentYear);
		
		//11. Number of followers on a social media platform: 1,000,000
		int followers = 1000000;
		System.out.println("11. Number of social media followers: "+followers);
		
		//12. Rating of a movie: 7.5
		float movieRating = 7.5f;
		System.out.println("12. Rating of a movie "+movieRating);
		
		//13. Person's blood type: 'A'
		char bloodType = 'A';
		System.out.println("13. Person's blood type: "+bloodType);
		
		//14. Title of a book: "To Kill a Mockingbird"
		String bookTitle = "To killa Mockingbird";
		System.out.println("14. Book Title: "+bookTitle);
		
		//15. Number of employees in a company: 500
		int empCount = 500;
		System.out.println("15. Number of employees in a company: "+empCount);
		
		//16.Time of an event: 2:30 PM
		String eventTime = "2:30 pm";
		System.out.println("16. Event Time: "+eventTime);
		
		//17. Name of a country: "United States"
		String country = "United States";
		System.out.println("17. Country Name: "+country);
		
		//18. Person's eye color: "Brown"
		String eyeColour = "Brown";
		System.out.println("18. Eye Colour: "+eyeColour);
		
		//19. Person's birthplace: "New York City"
		String birthPlace = "New York City";
		System.out.println("19. Person's birthplace: "+birthPlace);
		
		//20. Distance between two cities: 200.5
		float distance = 200.5f;
		System.out.println("20. Distance between two cities: "+distance+" km");
		
	}

}
